            _background(),
            _showForm(),
            _showCircularProgress(),
          ],
        ));
  }

  Widget _background(){

    return Container(
      color: Colors.grey.shade100
    );
  }

  Widget _showCircularProgress() {
    if (_isLoading) {
      return Center(child: CircularProgressIndicator());
    }
    return Container(
      height: 0.0,
      width: 0.0,
    );
  }

